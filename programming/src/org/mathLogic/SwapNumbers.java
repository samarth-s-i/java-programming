package org.mathLogic;

public class SwapNumbers {
    public static void main(String[] args) {
        int a,b,c;
        a=10;
        b=20;
        System.out.println("Before swappping:\na="+a+"\nb="+b);
        c=a;
        a=b;
        b=c;
        System.out.println("After swappping:\na="+a+"\nb="+b);
    }
}
